INCLUDE "EMU8086.INC"
.MODEL SMALL
.STACK 100H

.DATA 

YEAR DW 2009   

FOUR DB 4
HUN DW 100
FOURHUN DW 400
 
.CODE
MAIN PROC 
    MOV AX,@DATA
    MOV DS,AX
    
    MOV AX,YEAR
    ;MOV BX,FOURHUN
    DIV FOURHUN
    CMP DX,0
    JE LEAPYEAR
    JNE SECONECON 
    
    SECONECON:
    ;MOV AX,000H
    MOV AX,YEAR
    MOV DX,000H 
   
    MOV BL,FOUR 
    MOV BH,00H
    DIV BX
    MOV CX,DX
    CMP CX,0
    JE THIRDCON
    JNE NOTLEAPYEAR 
      
      
    THIRDCON:
    MOV AX,000H
    MOV AX,YEAR
    MOV BX,HUN
    DIV BX
    CMP DX,0
    JNE LEAPYEAR
    JE  NOTLEAPYEAR
    
       
    LEAPYEAR:
    PRINT "ITS LEAP YEAR"   
    JMP EXIT
    
    NOTLEAPYEAR:
    PRINT "ITS NOT A LEAP YEAR"
    
    EXIT:
    MOV AH,4CH
    INT 21H
    MAIN ENDP
END MAIN